<h3 class="comment">Comment me faire financer ?</h3>
<p id="etape">En premier lieu vous devez cliquez sur le boutton juste en haut après suivre les cinq autres étapes suivant puis envoyer. C'est après ces étapes que vous serez pris en compte.</p>
<br>
<h3 class="etap1">ÉTAPE 1</h3>
<div class="div1">
<img src="media/type.png" alt="" width="350">
&nbsp;
  <p>Une fois dans le formulaire d'information sur votre projet vous verrez un formulaire comme ceci la première étape conciste a choisis votre <span>Type</span> en cliquant dans la case marquer en jaune.</p>
</div>
<br>
<h3 class="etap2">ÉTAPE 2</h3>
<div class="div2">
<img src="media/nom.png" alt="" width="350">
&nbsp;
  <p>Toujours dans le formulaire d'information sur votre projet cette étape conciste a choisis votre <span>Nom</span> en cliquant dans la case marquer en jaune.</p>
</div>
<br>
<h3 class="etap3">ÉTAPE 3</h3>
<div class="div3">
<img src="media/secteur.png" alt="" width="350">
&nbsp;
  <p>La troisième conciste a choisis votre <span>Secteur</span> en cliquant dans la case marquer en jaune.</p>
</div >
<br>
<h3 class="etap4">ÉTAPE 4</h3>
<div class="div4">
<img src="media/description.png" alt="" width="350">
&nbsp;
  <p>Par la suite dans le même formulaire à cet étape vous donnerez tout la <span>Description</span> de votre projet en cliquant dans la case marquer en jaune.</p>
</div>
<br>
<h3 class="etap5">ÉTAPE 5</h3>
 <div  class="div5">
<img src="media/budget.png" alt="" width="350">
&nbsp;
  <p>Et enfin vous donnez le <span>Budget</span> pour la réalisation du projet en cliquant dans la case marquer en jaune.</p>
</div>
<br>
<h3 class="savoir">Comment savoir si mon projet est sélectionner ?</h3>
<br>
<div style="position: relavtive;">
<p class="projet">Pour savoir si votre projet est sélectionner il suffit de vous rendre dans le menu en cliquant  sur "<a href="#proje"><span>Liste des projects</span></a> " vous verrez la liste des projets sélectionner , si votre projet ne figure pas sur la liste cela veut dire que votre projet n'as pas été sélectionner, dans se cas attendez notre prochain crénaud.</p>
